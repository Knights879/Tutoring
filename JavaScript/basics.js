// DATA TYPES
// In JS, values are either primitives or objects and are dynamically typed. That
// means, you do not have to specify which type of variable you want to create,
// it will be implied by the data that variable holds (if the var holds a value
// of 5, it will be typed as a Number).
// NOTE: In JS, VALUES are typed, NOT VARIABLES.

// PRIMITIVE DATA TYPES
// 1. Number - Floating point
let num = 5;  // really 5.0
// 2. String - Sequence of characters
let str = "test";  // can use `'` or `"`
// 3. Boolean - `true` or `false`
let boolSwitch = true;
// 4. Undefined - When a variable has yet to be given a value
let und;
// 5. Null - Similar to undefined
let non = null;
// 6. Symbol - A unique value that cannot be changed
let sym;
// 7. BigInt - For integers that Number type cannot hold
let bigNum;

// We can check what the type of a value is by using `typeof`
